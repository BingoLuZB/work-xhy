/** 是否需要缓存？ */
export function needCache(url) {
    var cdnPath = window["wlmzCdn"];
    if (cdnPath && url && url.length>cdnPath.length && url.indexOf(cdnPath)==0) {
        var suffix = url.substr(cdnPath.length);
		var pos = suffix.indexOf("/");
		if (pos>=0) {
			suffix = suffix.substr(pos+1);
		}
        return suffix in cachefiles;
    }
    return false;
}

